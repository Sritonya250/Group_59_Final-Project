    public void populateTrainersTable(){
        DefaultTableModel model = (DefaultTableModel) tblTrainers.getModel();
        model.setRowCount(0);

        ArrayList<UserAccount> trainerList = enterprise.getOrganizationGivenRole(UserRole.SHELTER_TRAINER).getOrganizationUserDirectory().getUserAccountList();
        
        
        for(UserAccount user: trainerList) {
            ShelterTrainerProfile trainerProfile = (ShelterTrainerProfile) user.getAssociatedProfile();
            Object[] row = new Object[6];
            row[0] = user;
            row[1] = trainerProfile.getPerson().getFirstName();
            row[2] = trainerProfile.getPerson().getLastName();
            row[3] = trainerProfile.getPerson().getAge();
            row[4] = trainerProfile.getPerson().getGender().getValue();
            row[5] = user.getIsUserEnabled() ? "Enabled" : "Disabled";
            
            model.addRow(row);
        }
    }

