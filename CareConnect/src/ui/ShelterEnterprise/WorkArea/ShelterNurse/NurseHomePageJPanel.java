    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPatients.getSelectedRow();

        if(selectedRow < 0 ) {
            JOptionPane.showMessageDialog(this, "Please select a row to View!");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblPatients.getModel();
        UserAccount selectedPatientUser = (UserAccount) model.getValueAt(selectedRow, 0);

        PatientProfile patient = (PatientProfile) selectedPatientUser.getAssociatedProfile();
        
        if(patient.getIsCurrentlyAssignedToNurse() != null) {
            JOptionPane.showMessageDialog(this, "Patient is currently undergoing a check-up with a doctor!");
            return;
        }
        
        patient.setIsCurrentlyAssignedToNurse(currentAuthenticatedUser);
        JOptionPane.showMessageDialog(this, "Patient has been successfully assigned to you. Please check their Vitals!");

        populatePatientsTable();
    }//GEN-LAST:event_btnAssignActionPerformed


