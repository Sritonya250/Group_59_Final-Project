    public void populateTrainingModules(){
        DefaultTableModel model = (DefaultTableModel) tblTrainingModules.getModel();
        model.setRowCount(0);
        
        for(TrainingModule tm : this.currentAuthenticatedUser.getShelterTrainingModule().getTrainingModuleList()){
            Object[] row = new Object[4];
            row[0] = tm;
            row[1] = tm.getOpportunity().getJobTitle();
            row[2] = tm.getOpportunity().getRequiredSkill().getSkillName();
            row[3] = tm.getEnrolledPatientList().getPatientTrainerList().size();
            model.addRow(row);
        }
    }
    
    public void populatePatients(){
        DefaultTableModel model = (DefaultTableModel) tblPatients.getModel();
        model.setRowCount(0);
        
        PatientProfile patientProfile = null;
        for(UserAccount patientUser : business.getMasterPatientList().getUserAccountList()){
            patientProfile = (PatientProfile) patientUser.getAssociatedProfile();
            if(patientProfile.isIsTrainingReady() && !patientProfile.getEnrolledTrainingModules().isAlreadyEnrolled(selectedTrainingModule.getTrainingModuleId())){
                Object[] row = new Object[4];
                row[0] = patientProfile;
                row[1] = patientProfile.getPerson().getFirstName();
                row[2] = patientProfile.getPerson().getPhoneNum();
                row[3] = patientProfile.getPerson().getEmail();
                model.addRow(row);
            }
        }
    }

