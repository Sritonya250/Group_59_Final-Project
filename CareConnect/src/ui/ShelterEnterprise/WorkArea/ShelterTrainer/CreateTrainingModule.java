    private void btnCreateTrainingModuleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateTrainingModuleActionPerformed
        // TODO add your handling code here:
        
        if(selectedOpportunity == null) {
            JOptionPane.showMessageDialog(this, "Please select a Opportunity first!");
            return;
        }
            
        //Create Training Module at Shelter Training Enterprise Level and make a copy of the module for himself as well
        TrainingModule trainingModule = currentAuthenticatedUser.getShelterTrainingModule().createTrainingModule(currentAuthenticatedUser, selectedOpportunity);
        enterprise.getMasterTrainingModuleDirectory().addTrainingModule(trainingModule);
        
        this.selectedOpportunity =  null;
        txtJobDescription.setText("");
        
        JOptionPane.showMessageDialog(this, "New Training Module is successfully created");
    }//GEN-LAST:event_btnCreateTrainingModuleActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOpportunity.getSelectedRow();

        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from the table first to view the Job Opportunity details", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            Opportunity opportunity = (Opportunity) tblOpportunity.getValueAt(selectedRow, 0);
            this.selectedOpportunity = opportunity;
            txtJobDescription.setText(opportunity.getJobDesc());
        }
    }//GEN-LAST:event_btnViewActionPerformed


