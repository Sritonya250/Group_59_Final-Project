    public void populateOpportunityTable(){
        DefaultTableModel model = (DefaultTableModel) tblOpportunity.getModel();
        model.setRowCount(0);
        
        //find the CSR Enterprise
        CSREnterprise csrEnterprise = null;
        for(Enterprise e: model.getEnterpriseCatalog().getEnterpriseList()){
            if(e instanceof CSREnterprise){
                csrEnterprise = (CSREnterprise) e;
            }
        }
        
        for(Opportunity o : csrEnterprise.getMasterOpportunityCatalog().getOpportunityList()){
            Object[] row = new Object[4];
            row[0] = o;
            row[1] = o.getJobTitle();
            row[2] = o.getRequiredSkill().getSkillName();
            row[3] = o.getAvailablePositions();
            model.addRow(row);
        }

    }

