    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String nameOfNurse = nurseName.getText();
        
        if(selectedNurseUser == null) {
            JOptionPane.showMessageDialog(this, "Please select a Nurse first!");
            return;
        }

        if(nameOfNurse.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please select a Nurse first!");
            return;
        }

        String isEnabledString = enableUserDropDown.getSelectedItem().toString();

        NurseStation selectedNurseStation = (NurseStation) availableStationsDropDown.getSelectedItem();

        ShelterNurseProfile nurse = (ShelterNurseProfile) selectedNurseUser.getAssociatedProfile();

        this.selectedNurseUser.setIsUserEnabled((isEnabledString == "Yes") ? true : false);
        nurse.setAssignedNurseStation(selectedNurseStation);

        this.selectedNurseUser =  null;

        JOptionPane.showMessageDialog(this, "Nurses information has been updated successfully!!!");
        populateNurseTable();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblNurse.getSelectedRow();
        this.selectedNurseUser = null;
        
        if(selectedRow < 0 ) {
            JOptionPane.showMessageDialog(this, "Please select a row to View!");
            return;
        } else {
                            
        DefaultTableModel model = (DefaultTableModel) tblNurse.getModel();
        UserAccount user = (UserAccount) model.getValueAt(selectedRow, 0);
        
        ShelterNurseProfile nurse = (ShelterNurseProfile) user.getAssociatedProfile();
        this.selectedNurseUser = user;
        nurseName.setText(nurse.getPerson().getFirstName());
        }
    }//GEN-LAST:event_btnViewActionPerformed


