     public void populateData() {
        pageLabel.setText("Welcome Nurse, " + currentAuthenticatedUser.getPerson().getFirstName());
        DateTimeFormatter dateFormat = DateTimeFormatter.ofPattern("MM/dd/yyyy HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        currentDate.setText("Date: " + dateFormat.format(now));
        
        labelStationArea.setText(currentAuthenticatedUser.getAssignedNurseStation().getName());
    }
     
     public void populatePatientsTable() {
        DefaultTableModel model = (DefaultTableModel) tblPatients.getModel();
        model.setRowCount(0);
        
        ArrayList<UserAccount> users = model.getMasterPatientList().getUserAccountList();
        
        for(UserAccount user: users) {
            PatientProfile patient = (PatientProfile) user.getAssociatedProfile();
            if(patient.getIsCurrentlyAssignedToNurse() == null && patient.isNeedsDoctorAttention() == false){
                Object[] row = new Object[4];
                row[0] = user;
                row[1] = patient.getPerson().getFirstName();
                row[2] = patient.getPerson().getPhoneNum();
                row[3] = patient.getIsCurrentlyAssignedToDoctor() != null ? true : false;
                model.addRow(row);
            }
        }
     }

