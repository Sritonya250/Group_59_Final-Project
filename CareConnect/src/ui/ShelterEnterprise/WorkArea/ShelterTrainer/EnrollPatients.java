    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblTrainingModules.getSelectedRow();

        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from the table first to view the Training Module details", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            TrainingModule tm = (TrainingModule) tblTrainingModules.getValueAt(selectedRow, 0);
            this.selectedTrainingModule = tm;
            txtJobDescription.setText(selectedTrainingModule.getOpportunity().getJobDesc());
        }
        populatePatients();
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnAssignModuleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignModuleActionPerformed
        // TODO add your handling code here:
        if(selectedPatient != null && selectedTrainingModule != null){
            //Assign module to patient
            PatientTrainer pt = selectedPatient.getEnrolledTrainingModules().createPatientTrainer(selectedPatient, selectedTrainingModule);
            
            //Update enrolled patient list in training module object
            selectedTrainingModule.getEnrolledPatientList().addPatientTrainer(pt);
            
            this.selectedPatient = null;
            txtPatientName.setText("");
            
            populatePatients();
        }
        else{
            JOptionPane.showMessageDialog(this, "Please select the training module and the patient you wish to enroll", "Warning", JOptionPane.WARNING_MESSAGE);
            return;  
        }
    }//GEN-LAST:event_btnAssignModuleActionPerformed

    private void btnViewPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewPatientActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPatients.getSelectedRow();

        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Patient to enroll in the Training Program!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            PatientProfile patientProfile = (PatientProfile) tblPatients.getValueAt(selectedRow, 0);
            this.selectedPatient = patientProfile;
            txtPatientName.setText(selectedPatient.getPerson().getFirstName() + " " + selectedPatient.getPerson().getLastName());
        }
    }//GEN-LAST:event_btnViewPatientActionPerformed


