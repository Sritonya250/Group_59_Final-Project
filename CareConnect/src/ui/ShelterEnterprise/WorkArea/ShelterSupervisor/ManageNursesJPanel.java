    public void populateNurseTable() {
        DefaultTableModel model = (DefaultTableModel) tblNurse.getModel();
        model.setRowCount(0);

        ArrayList<UserAccount> nurseList = enterprise.getOrganizationGivenRole(UserRole.SHELTER_NURSE).getOrganizationUserDirectory().getUserAccountList();
        
        
        for(UserAccount user: nurseList) {
            ShelterNurseProfile nurseProfile = (ShelterNurseProfile) user.getAssociatedProfile();
            Object[] row = new Object[4];
            row[0] = user;
            row[1] = nurseProfile.getPerson().getEmail();
            row[2] = nurseProfile.getPerson().getPhoneNum();
            row[3] = nurseProfile.getAssignedNurseStation() != null ? nurseProfile.getAssignedNurseStation() : "-";
            model.addRow(row);
        }
    }

