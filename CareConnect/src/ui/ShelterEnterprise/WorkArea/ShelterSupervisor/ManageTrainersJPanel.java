    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblTrainers.getSelectedRow();
        this.selectedTrainer = null;
        
        if(selectedRow < 0 ) {
            JOptionPane.showMessageDialog(this, "Please select a Trainer from the table");
            return;
        } else {
                            
        DefaultTableModel model = (DefaultTableModel) tblTrainers.getModel();
        UserAccount user = (UserAccount) model.getValueAt(selectedRow, 0);
        
        ShelterTrainerProfile trainer = (ShelterTrainerProfile) user.getAssociatedProfile();
        this.selectedTrainer = user;
        lblNameValue.setText(trainer.getPerson().getFirstName());
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        if(selectedTrainer == null) {
            JOptionPane.showMessageDialog(this, "Please select a Trainer first!");
            return;
        }

        String isEnabledString = enableUserDropDown.getSelectedItem().toString();


        ShelterTrainerProfile trainer = (ShelterTrainerProfile) selectedTrainer.getAssociatedProfile();

        this.selectedTrainer.setIsUserEnabled((isEnabledString == "Yes") ? true : false);

        this.selectedTrainer =  null;

        JOptionPane.showMessageDialog(this, "Trainer information has been updated successfully!!!");
        populateTrainersTable();
    }//GEN-LAST:event_btnSaveActionPerformed

