    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblManageEmployers.getModel();
        model.setRowCount(0);
        
        ArrayList<Organization> organizations = enterprise.getOrganizationCatalog().getOrganizationList();
        
        for(Organization org: organizations) {
           ArrayList<UserAccount> usersAccounts = org.getOrganizationUserDirectory().getUserAccountList();
           
           for(UserAccount user: usersAccounts) {
               if(user.getAssociatedProfile() instanceof CSREmployerProfile){
                   CSREmployerProfile csrEmployer = (CSREmployerProfile) user.getAssociatedProfile();
                    Object[] row = new Object[4];
                    row[0] = user;
                    row[1] = csrEmployer.getPerson().getFirstName();
                    row[2] = csrEmployer.getLicense();
                    row[3] = user.getIsUserEnabled() ? "Enabled" : "Disabled";
                    model.addRow(row);
                }
           }
        }
    }

