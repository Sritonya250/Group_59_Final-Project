    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManageEmployers.getSelectedRow();

        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from the table first to view the Employer details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            UserAccount user = (UserAccount) tblManageEmployers.getValueAt(selectedRow, 0);
            CSREmployerProfile employer = (CSREmployerProfile) user.getAssociatedProfile();
            this.selectedEmployerUser = user;
            txtEmployerName.setText(employer.getPerson().getFirstName());
        }
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String nameOfEmployer = txtEmployerName.getText();
        
        if(nameOfEmployer.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please select an Employer first!");
            return;
        }
        
        String isEnabledString = enableUserDropDown.getSelectedItem().toString();
        
        
        CSREmployerProfile employer = (CSREmployerProfile) selectedEmployerUser.getAssociatedProfile();
        
        this.selectedEmployerUser.setIsUserEnabled((isEnabledString == "Yes") ? true : false);
        
        this.selectedEmployerUser =  null;
        
        JOptionPane.showMessageDialog(this, "Employer's information has been updated successfully!!!");
        populateTable();
    }//GEN-LAST:event_btnSaveActionPerformed


