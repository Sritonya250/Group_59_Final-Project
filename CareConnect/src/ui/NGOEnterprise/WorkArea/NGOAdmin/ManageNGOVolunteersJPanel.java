    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) volunteersTable.getModel();
        model.setRowCount(0);
        
        ArrayList<Organization> organizations = enterprise.getOrganizationCatalog().getOrganizationList();
        
        for(Organization org: organizations) {
           ArrayList<UserAccount> usersAccounts = org.getOrganizationUserDirectory().getUserAccountList();
           
           for(UserAccount user: usersAccounts) {
               if(user.getAssociatedProfile() instanceof NGOVolunteerProfile){
                   NGOVolunteerProfile ngoVolunteer = (NGOVolunteerProfile) user.getAssociatedProfile();
                    Object[] row = new Object[4];
                    row[0] = user;
                    row[1] = ngoVolunteer.getPerson().getEmail();
                    row[2] = ngoVolunteer.getPerson().getPhoneNum();
                    row[3] = ngoVolunteer.getAssignedNeighbourhood() != null ? ngoVolunteer.getAssignedNeighbourhood() : "-";
                    model.addRow(row);
                }
           }
        }
    }

