    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String nameOfVolunteer = volunteerName.getText();
        
        if(nameOfVolunteer.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please select a Volunteer first!");
            return;
        }
        
        String isEnabledString = enableUserDropDown.getSelectedItem().toString();
        
        Neighbourhood selectedNeighbourhood = (Neighbourhood) availableAreasDropDown.getSelectedItem();
        
        NGOVolunteerProfile volunteer = (NGOVolunteerProfile) selectedVolunteerUser.getAssociatedProfile();
        
        this.selectedVolunteerUser.setIsUserEnabled((isEnabledString == "Yes") ? true : false);
        volunteer.setAssignedNeighbourhood(selectedNeighbourhood);
        
        
        this.selectedVolunteerUser =  null;
        
        JOptionPane.showMessageDialog(this, "Volunteer's information has been updated successfully!!!");
        populateTable();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnViewVolunteerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewVolunteerActionPerformed
        // TODO add your handling code here:
        int selectedRow = volunteersTable.getSelectedRow();

        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from the table first to view the Volunteers details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            UserAccount user = (UserAccount) volunteersTable.getValueAt(selectedRow, 0);
            NGOVolunteerProfile volunteer = (NGOVolunteerProfile) user.getAssociatedProfile();
            this.selectedVolunteerUser = user;
            volunteerName.setText(volunteer.getPerson().getFirstName());
        }
    }//GEN-LAST:event_btnViewVolunteerActionPerformed


